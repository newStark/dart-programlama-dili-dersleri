main() {
//  If Else

  var sayi = 1;

  if (sayi > 5) {
    print('Sayı değişkeni 5\'ten büyüktür.');
  } else {
    print('diğer durumlar... 1');
  }

  var deneme = 9;

  if (deneme == 9) {
    print("deneme değeri 9'a eşit");
  }

  if (deneme > 3) {
    print("deneme değeri 3'ten büyük..");
  }

  if (deneme > 4) {
    print("deneme değeri 4'ten büyük..");
  }

  if (deneme > 6) {
    print("deneme değeri 6'ten büyük..");
  } else if (deneme > 7) {
    print("deneme değeri 7'den büyük..");
  } else {
    print('diğer durumlar... 2');
  }
}
