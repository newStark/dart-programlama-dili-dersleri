main() {
//  Switch-Case

  var bugun = 'Salı';

  switch (bugun) {
    case 'Cumartesi':
      print('Bugün dükkanımız kapalıdır.');
      break;

    case 'Pazar':
      print('Bugün dükkanımız kapalıdır, lütfen yarın geliniz...');
      break;

    default:
      print('Bugün dükkanımız AÇIK');
  }
}
